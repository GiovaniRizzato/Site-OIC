    <p>A <b>Organização Internacional de  Consciencioterapia (OIC)</b> é  uma Instituição Conscienciocêntrica especializada em <a href="consciencioterapia">Consciencioterapia</a>, sem  fins lucrativos e mantida pelo trabalho de seus voluntários.</p>
    <p>&nbsp;</p>
    <p>A OIC foi criada para ser  organização de excelência em Consciencioterapia, referência na formação e no  aperfeiçoamento de consciencioterapeutas e para auxiliar na implantação do  holopensene de saúde consciencial neste planeta.</p>
<p>&nbsp;</p>   

  <p><b>Principais objetivos:</b></p>
  <ul>
    <li>Prestar assistência às consciências, auxiliando-as       na autocura e estimulando sua saúde integral.</li>
    <li>Pesquisar, estudar, divulgar, supervisionar e       estimular a saúde consciencial e os fenômenos físicos e parapsíquicos       interligados à Consciencioterapia.</li>
    <li>Fomentar, produzir e promover atividades e       projetos científicos para a difusão da Consciencioterapia.</li>
    <li>Conciliar as pesquisas consciencioterápicas       com abordagens interdisciplinares, em uma visão universalista do ser       humano (consciência).</li>
    <li>Atuar na formação e aperfeiçoamento de       pesquisadores, educadores, consciencioterapeutas e epicentros       conscienciais.</li>
  </ul>
<p>&nbsp;</p>

<p>Tais objetivos foram firmados em estatuto, aprovados na Assembleia Geral de fundação da OIC, no dia 6 de setembro de 2003.</p>
